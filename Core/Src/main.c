/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint32_t readings[DATA_COLLECT_TOTAL_NUM_ANALOG_CHANNELS];
  uint32_t last_reading_tick = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  DebugIO_Init(&huart1);
  __HAL_DBGMCU_FREEZE_TIM3(); // Pause timer 3 (ADC trigger timer) on debug
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_ADC3_Init();
  MX_ADC4_Init();
  MX_USART1_UART_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
  DataCollect_PrepareHardware(&hadc1, &hadc2, &hadc3, &hadc4);
  resetTerminal();
  DataCollect_Start(&htim3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // Wait for new set of readings
    while (!DataCollect_Poll());

    DataCollect_Get(readings);

    // Blink LED on data transmit
    last_reading_tick = HAL_GetTick();
    HAL_GPIO_WritePin(BOARD_LED_GPIO_Port, BOARD_LED_Pin, LED_ON);

    for (int i = 0; i < DATA_COLLECT_TOTAL_NUM_ANALOG_CHANNELS - 1; i++)
    {
      printf("%lu,", readings[i]);
    }
    printf("%lu;\r\n", readings[DATA_COLLECT_TOTAL_NUM_ANALOG_CHANNELS - 1]);

    while (HAL_GetTick() - last_reading_tick < 100);
    HAL_GPIO_WritePin(BOARD_LED_GPIO_Port, BOARD_LED_Pin, LED_OFF);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

